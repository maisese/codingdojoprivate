public class FizzBuzz {
    public void happen(int number) {

        if (number == 3) {
            System.out.println("Fizz");
        }
        if (number == 5) {
            System.out.println("Buzz");
        }
        if (number == 15) {
            System.out.println("Fizz Buzz");
        }
        if (number == 2) {
            System.out.println("2");
        }

    }
}